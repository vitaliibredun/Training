package variables;

public class variables {
    public static void main(String[] args) {
        int myInt = 476;    //integers and 32 bit
        short myShort = 2324;   //16 bit or -32 000 to +32 000
        long myLong = 343423;   //64 bit

        double myDouble = 234.56;   //64 bit of real numbers
        float myFloat = -85.4f;

        char myChar = 'f';  //symbols
        boolean myBoolean = true;   //true or false

        byte myByte = 77;   //8 bit -128 to 128

        System.out.println(myInt);
    }
}
